            gradilisteDb.addNewGradiliste(jTextFieldGradiliste.getText(), jTextFieldMesto.getText(), jTextFieldAdresa.getText());
            jTextFieldGradiliste.setText("");
            jTextFieldMesto.setText("");
            jTextFieldAdresa.setText("");
            readGradiliste();
