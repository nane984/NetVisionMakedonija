    private void readGradiliste() {

        List<Db.Gradiliste> result = gradilisteDb.getGradilista();
        Object[][] obj = new Object[result.size()][4];

        int i = 0;

        for (Db.Gradiliste auto : result) {
            obj[i][0] = i + 1;
            obj[i][1] = auto.getGradiliste();
            obj[i][2] = auto.getMesto();
            obj[i][3] = auto.getAdresa();

            i++;
        }

        DefaultTableModel model = new DefaultTableModel(obj, new Object[]{"r.b.", "Gradiliste", "Mesto", "Adresa"});
        jTable1.setModel(model);
        jTable1.setRowMargin(4);
        jTable1.setRowHeight(40);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(45);

    }

    private void addFilterFradiliste() {
        jTextFieldGradiliste.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = jTextFieldGradiliste.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                String text = jTextFieldGradiliste.getText();

                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }

        });
    }
